void solve() {
	int n; cin>>n;

	int res = INT_MAX;
	for(int i : twoPowers) {

		int taken = 0;
		string s1 = to_string(n), s2 = to_string(i);
		int p1 = 0, p2 = 0;
		while(p1 < s1.length() && p2 < s2.length()) {
			if(s1[p1] == s2[p2]) {
				p1++;
				p2++;
				taken++;
			} else {
				p1++;
			}
		}

		res = min(res, (long long)(s1.length() + s2.length() - 2*taken));
	}

	cout<<res<<endl;
}
 
 
 
 
